            <h3>Направления подготовки</h3>
            <div class="faq-body first-level">
                <span>
                    <strong>— Какие новые направления подготовки (специальности) были открыты в ДонГТИ?</strong><br>
                    ДонГТИ приглашает получить высшее образование по новым перспективным направлениям подготовки и специальностям:
                    <ul>
                        <li>прикладная механика (проектно-конструкторское обеспечение машиностроительных производств);</li>
                        <li>конструирование и технология электронных средств (компьютерное проектирование систем силовой электроники);</li>
                        <li>информационная безопасность автоматизированных систем (обеспечение информационной безопасности распределенных информационных систем);</li>
                        <li>математика и компьютерные науки (цифровые технологии в бизнесе (по отраслям));</li>
                        <li>дизайн архитектурной среды (дизайн архитектурной среды промышленных и социальных объектов);</li>
                        <li>экология и природопользование (прикладная экология и природопользование);</li>
                        <li>строительство (строительство, эксплуатация и реконструкция промышленных объектов, зданий и сооружений; стоимостной инжиниринг в строительстве; строительство автомобильных дорог и дорожных объектов; строительство зданий и сооружений);</li>
                        <li>автоматизация технологических процессов и производств (управление и инновации в автоматизированных системах и технологических процессах; автоматизация и управление дорожно-транспортной инфраструктурой; автоматизация инженерных и строительных технологий).</li>
                    </ul>
                    <strong>— Сколько бюджетных мест было выделено по направлениям подготовки?</strong><br>
                    Данные на 2022 г. можно посмотреть по <a href="https://www.dstu.education/files/selection_committee/2021/app1.pdf" target="_blank">ссылке</a>.

