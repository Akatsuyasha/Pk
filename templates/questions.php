            <h3>Прочие вопросы</h3>
            <div class="faq-body first-level">
                <span>
                    <strong>— Есть ли военная кафедра? Все ли студенты могут там учиться.</strong><br>
                    В ГОУ ВО ЛНР «ДонГТИ» есть военная кафедра. Обучение на военной кафедре могут пройти студенты, которые учатся на 1 курсе магистратуры или 4 курсе специалитета очной формы.<br><br>

                    <strong>— Можно ли поступить на 2-3 курс после колледжа?</strong><br>
                    В соответствии с порядком приема и правилами приема в институт Вы можете поступить на 2-3 курс, если имеете диплом специалиста среднего звена.<br><br>

                    <strong>— Можно ли поступить на ускоренную программу обучения после среднего профессионального образования (специалист среднего звена)?</strong><br>
                    Да. Вы можете поступить на ускоренную программу обучения, если такая форма обучения предусмотрена на данном направлении.<br><br>

                    <strong>— Как получить второе высшее образование?</strong><br>
                    Вам необходимо обратиться в деканат факультета, подать заявление на то направление и форму обучения, которые Вас интересуют, зачисление происходит по собеседованию, финансирование за счет средств физических и (или) юридических лиц.<br><br>

                    <strong>— Какой размер стипендии?</strong><br>
                    Минимальная государственная стипендия (1-й семестр) - 2100 руб. Далее стипендия назначается по результатам сессии.<br><br>

                    <strong>— Как можно к вам перевестись?</strong><br>
                    Приемная комиссия не занимается переводами. Это прерогатива факультетов. Вам следует обратиться в деканат того факультета, куда Вы хотите переводиться. Детали узнавайте в деканате. Координаты факультетов смотрите на странице <a href="index.php#fh5co-features" data-nav-section="features">факультета</a>.
