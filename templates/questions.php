            <h3>Особые права и преимущества</h3>
            <div class="faq-body first-level">
                <span>
                    <strong>— Кому предоставляется право на зачисление вне конкурса в рамках особой квоты?</strong><br>
                    Право на зачисление вне конкурса в рамках особой квоты и право первоочередного зачисления при участии в общем конкурсном отборе (при поступлении на базе среднего общего образования; на базе среднего профессионального образования) предоставляется следующим категориям абитуриентов:<br>
                    <ol class="bracket-list">
                        <li>дети-сироты; дети, лишенные родительской опеки; лица из числа детей-сирот; лица из числа детей, лишенных родительской опеки;</li>
                        <li>дети-инвалиды; инвалиды I и II групп; инвалиды с детства (примечание: эти категории лиц должны предоставить заключение учреждения медико-социальной экспертизы о том, что им не противопоказано обучение по данному направлению подготовки/специальности в ДонГТИ);</li>
                        <li>инвалиды вследствие военной травмы или заболевания, полученных в период прохождения военной службы (примечание: эти категории лиц должны предоставить заключение учреждения медико-социальной экспертизы о том, что им не противопоказано обучение по данному направлению подготовки/специальности в ДонГТИ);</li>
                        <li>военнослужащие Народной милиции ЛНР, которые проходят военную службу по контракту; граждане, прошедшие военную службу по призыву и поступающие на обучение по рекомендациям командиров;</li>
                        <li>лица, пострадавшие вследствие Чернобыльской катастрофы, которым согласно действующему законодательству ЛНР предоставлено такое право;</li>
                        <li>лица, которым предоставлено такое право согласно Горному закону ЛНР (статья 43 Горного закона ЛНР с изменениями);</li>
                        <li>лица, на которых распространяется действие Закона ЛНР от 21.02.2017 №147-II «Об участниках боевых действий в период отражения вооруженной агрессии Украины против Луганской Народной Республики»;</li>
                        <li>дети погибших (умерших) во время исполнения служебных обязанностей лиц рядового и начальствующего состава службы гражданской защиты и работников основного личного состава профессиональных аварийно-спасательных служб.</li>
                    </ol>
                    <strong>— Кому предоставляется право на зачисление вне конкурса в рамках профессиональной квоты?</strong><br>
                    Право на зачисление вне конкурса в рамках профессиональной квоты имеют следующие категории абитуриентов (при поступлении на базе среднего общего образования):
                    <ul>
                        <li>лица, успешно окончившие подготовительные курсы ДонГТИ (объем учебных часов – не меньше 150 аудиторных часов и срок обучения – не меньше трех месяцев);</li>
                        <li>выпускники базовых школ ДонГТИ, успешно сдавшие выпускные экзамены (примечание: базовыми считаются школы, с которыми ДонГТИ заключил договор о сотрудничестве).</li>
                    </ul>
                    <strong>— Кому предоставляется право на зачисление по собеседованию в рамках профессиональной квоты?</strong><br>
                    Право на зачисление по собеседованию в рамках профессиональной квоты имеют следующие категории абитуриентов (при поступлении на базе среднего общего образования или на базе среднего профессионального образования):
                    <ul>
                        <li>выпускники общеобразовательной организации, получившие среднее общее образование, награжденные золотой или серебряной медалями «За особые успехи в учении», – при поступлении на основе среднего общего образования;</li>
                        <li>выпускники образовательных учреждений среднего профессионального образования, получившие диплом с отличием, – при поступлении для получения уровня профессионального образования бакалавра или специалиста;</li>
                        <li>победители и призеры заключительного этапа Республиканской олимпиады школьников (при условии, что профиль олимпиады и профильный предмет при поступлении в ДонГТИ соответствуют друг другу); победители республиканского конкурса «Конкурс-защита научно-исследовательских работ НО «РМАН», проводимого МОН ЛНР и Государственным учреждением дополнительного образования ЛНР «Республиканский центр научно-технического творчества» (при условии, что секция, по которой абитуриент принимал участие в конкурсе-защите, соответствует направлению подготовки (специальности) при поступлении в ДонГТИ); члены сборных команд ЛНР, участвовавшие в международных олимпиадах по общеобразовательным предметам, по направлениям подготовки и/или специальностям (при условии, что профиль олимпиады и профильный предмет при поступлении в ДонГТИ соответствуют друг другу).</li>
                    </ul>

