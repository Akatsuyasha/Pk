            <h3>Вопросы по документам</h3>
            <div class="faq-body first-level first-level">
                <span><strong>— Как можно подать документы для поступления?</strong><br>
                    а) лично;<br>
                    б) на сайте вуза через автоматизированную систему («Электронное заявление»);<br>
                    в) по доверенности другим лицом.<br><br>

                    <strong>— Какие документы нужны для поступления?</strong><br>
                    Перечень документов, необходимый для поступления, можно посмотреть по <a href="structure.php?page=documents#content-start">ссылке</a>.<br><br>

                    <strong>— Нужна ли медицинская справка для поступления?</strong><br>
                    При подаче документов необходимо предоставить оригинал медицинской справки формы 086-у или ее копию.<br><br>

                    <strong>— Нужно ли заверять копии документов у нотариуса?</strong><br>
                    Все копии документов заверяются по предъявленным оригиналам документов приемной комиссией образовательного учреждения, в которое они подаются, или в установленном законодательством порядке.<br><br>

                    <strong>— Может ли за меня подать документы мама (другие родственники)?</strong><br>
                    Да. По доверенности, которая оформлена в установленном законодательством порядке.<br><br>

                    <strong>— Когда нужно подать заявление о согласии на зачисление?</strong><br>
                    До установленной в правилах приема даты.<br><br>
