            <h3>Поступление в магистратуру</h3>
            <div class="faq-body first-level">
                <span>
                    <strong>— Какие документы нужны для поступления?</strong><br>
                    Перечень документов, необходимых для поступления в магистратуру:
                    <ul>
                        <li>заявление от абитуриента;</li>
                        <li>документ об образовании с приложением к нему, на основе которого осуществляется поступление, предъявляется лично, подается оригинал и копия (2 экз.);</li>
                        <li>медицинская справка формы 086-у предъявляется лично, подается оригинал или копия (1 экз.);</li>
                        <li>карта прививок формы 063 – для лиц, которые поступают на очную форму обучения;</li>
                        <li>копия регистрационного номера учетной карточки плательщика налогов (РНУКПН) (2 экз.);</li>
                        <li>шесть цветных фотографий размером 3x4 см;</li>
                        <li>документ об изменении фамилии (в случае разных фамилий в паспорте и в документе об образовании), подается копия (2 экз.);</li>
                        <li>трудовая книжка, при ее наличии, подается копия (1 экз.), заверенная в установленном порядке – для лиц, которые поступают на заочную форму обучения;</li>
                        <li>документ, удостоверяющий личность (паспорт или свидетельство о рождении для лиц, которые по возрасту не имеют паспорта), предъявляется лично, подается копия (2 экз.);</li>
                        <li>документ воинского учета (военный билет или приписное свидетельство), предъявляется оригинал и подается копия (1 экз.)</li>
                    </ul>
                    Для абитуриентов, которые закончили обучение в год поступления в магистратуру и получили степень бакалавра (специалиста) в ДонГТИ, перечень необходимых документов следует уточнить в деканате факультета.<br><br>

                    <strong>— Куда необходимо подавать документы при поступлении в магистратуру?</strong><br>
                    Вам необходимо подать документы в деканат того факультета, куда хотите поступать. Если Вы подаете документы в субботу, обращайтесь в приемную комиссию ГОУ ВО ЛНР «ДонГТИ».<br><br>

                    <strong>— Может ли специалист поступать на бюджетное место?</strong><br>
                    Не может претендовать на бюджетное место "специалист" – лицо, закончившее высшее учебное заведение.<br><br>

                    <strong>— Существуют ли льготы при поступлении в магистратуру?</strong><br>
                    Нет.<br><br>

                    <strong>— Сколько направлений подготовки можно выбрать для участия в конкурсе?</strong><br>
                    Не более 1-го.<br><br>

                    <strong>— Какие вступительные испытания необходимо пройти?</strong><br>
                    Два вступительных испытания – иностранный язык и профессиональный аттестационный экзамен.<br><br>

                    <strong>— Где можно посмотреть перечень тем и вопросов для подготовки к вступительному экзамену?</strong><br>
                    Перечень тем и вопросов для подготовки к вступительным испытаниям вы можете посмотреть на официальном сайте ГОУ ВО ЛНР «ДонГТИ» в разделе «АБИТУРИЕНТУ – Приемная комиссия (Вступительные испытания - Программы вступительных и профессиональных аттестационных экзаменов при поступлении в ГОУ ВО ЛНР «ДонГТИ»).<br><br>

                    <strong>— Как производится зачисление на бюджетные места?</strong><br>
                    По результатам вступительных испытаний. По конкурсу зачисляются поступающие, имеющие наивысшие баллы, предоставившие оригинал документа об образовании и заявление о согласии на зачисление.<br><br>

                    <strong>— Может ли студент-магистр пройти обучение на военной кафедре?</strong><br>
                    Да.
